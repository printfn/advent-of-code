let res = 0;
let completionscores = [];
outer: for (let line of input) {
    let stack = [];
    for (let ch of line) {
        switch (ch) {
            case '(': stack.push(')'); break;
            case '[': stack.push(']'); break;
            case '{': stack.push('}'); break;
            case '<': stack.push('>'); break;
            default:
                let c = stack.pop();
                if (ch != c) {
                    if (ch == ')') {
                        res += 3;
                    } else if (ch == ']') {
                        res += 57;
                    } else if (ch == '}') {
                        res += 1197;
                    } else if (ch == '>') {
                        res += 25137;
                    }
                    continue outer;
                }
                break;
        }
    }
    // autocomplete!
    let completionscore = 0;
    while (stack.length != 0) {
        let ch = stack.pop();
        completionscore *= 5;
        if (ch == ')') {
            completionscore += 1;
        } else if (ch == ']') {
            completionscore += 2;
        } else if (ch == '}') {
            completionscore += 3;
        } else if (ch == '>') {
            completionscore += 4;
        }
    }
    completionscores.push(completionscore);
}
